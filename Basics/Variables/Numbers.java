// What you'll learn:
// How to use numbers in Java.

public class Numbers
{
    /*
        There are 2 different ways of creating numbers in java

        The first one is an "int" or "integer", which is a whole number value
        with no decimal
        int x = 5;

        The second way to create a number in Java is through a float/double, which
        can be either a whole number or decimal value
        float y = 10;
        double z = 15;

        -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
        -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=

        This will print out our variables we have talked about
    */
    public static void main(String[] args)
    {
        int x = 5;
        float y = 10;
        double z = 15;

        System.out.println("int value: " + x);
        System.out.println("float value: " + y);
        System.out.println("double value: " + z);
    }
}
